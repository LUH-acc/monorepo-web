function randomLowerAlphanumeric(length: number): string {
    const chars = 'abcdefghijklmnopqrstuvwxyz0123456789'
    return Array.from({ length }, () => chars[Math.floor(Math.random() * chars.length)]).join('')
}

// 获取文字宽度
function getTextWidth(ctx: CanvasRenderingContext2D): number {
    const metrics = ctx.measureText('#')
    return metrics.width + 2
}

// 渲染到 canvas（坐标用 CSS 逻辑像素，由 setTransform(dpr) 负责高清缩放）
function renderCanvas(canvasTextList: string[][], ctx: CanvasRenderingContext2D, dpr: number) {
    const textW = getTextWidth(ctx)
    for (let i = 0; i < canvasTextList.length; i++) {
        const charList = canvasTextList[i]!
        for (let j = 0; j < charList.length; j++) {
            let char = charList[j] || ''

            if (char.startsWith('MAIN_')) {
                char = char.replace('MAIN_', '')
                ctx.fillStyle = '#41B1FF'
            } else {
                ctx.fillStyle = '#485F7F'
            }

            ctx.fillText(char, j * textW * dpr, (i + 1) * LINE_HEIGHT * dpr)
        }
    }
}
let timer = null
const drawText = () => {
    const canvas = canvasRef.value
    if (!canvas) return
    const ctx = canvas.getContext('2d')
    if (!ctx) return
    // 获取定位字符转换成数组坐标
    let canvasTextList = canvasChar.split('\n').map((item) => item.split('').map(char => {
        if (char === ',') {
            return randomLowerAlphanumeric(1)
        } else {
            return 'MAIN_' + char
        }
    }))

    const w = canvasTextList[0]?.length || 100
    const h = canvasTextList.length

    const dpr = window.devicePixelRatio || 1
    ctx.font = `${12 * dpr}px ui-monospace, monospace`
    const textW = getTextWidth(ctx)
    const logicalW = w * textW
    const logicalH = h * LINE_HEIGHT + LINE_HEIGHT / 2

    // 物理像素 = 逻辑尺寸 × dpr，保证 Retina 清晰
    canvas.width = logicalW * dpr
    canvas.height = logicalH * dpr
    canvas.style.width = `${logicalW}px`
    canvas.style.height = `${logicalH}px`

    ctx.clearRect(0, 0, canvas.width, canvas.height)

    timer = setInterval(() => {
        ctx.clearRect(0, 0, canvas.width  , canvas.height)
        for(let i = 0; i < 100; i++) {
            const y = Math.floor(Math.random() * h)
            const x = Math.floor(Math.random() * w)
            const char = canvasTextList[y]?.[x] || ''
            if(char.startsWith('MAIN_')) {
                i--
                continue
            } else {
                canvasTextList[y][x] = randomLowerAlphanumeric(1)
            }
        }
        renderCanvas(canvasTextList, ctx,dpr)
    }, 100)


}

